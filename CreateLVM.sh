#!/bin/bash

lsblk

echo -e "\nEnter y or n:"

read var

if [[ "$var" == "y" ]]
    then
    echo "#####################################################################"
    pvcreate /dev/sdb /dev/sdc /dev/sdd
    echo "#####################################################################"
    vgcreate vgdata /dev/sdb /dev/sdc /dev/sdd
    echo "#####################################################################"
    lvcreate -n lvdata -l +95%FREE vgdata
    echo "#####################################################################"
    mkdir -v /data
    echo "#####################################################################"
    mkfs.xfs /dev/mapper/vgdata-lvdata
    echo "#####################################################################"
    mount -v /dev/mapper/vgdata-lvdata /data/
    echo "#####################################################################"
    echo -e "/dev/mapper/vgdata-lvdata                 /data                   xfs     defaults,noatime,nodiratime     0 0" >> /etc/fstab
    cat /etc/fstab
    echo "#####################################################################"
    lsblk
    echo "#####################################################################"
    fdisk -l
    echo "#####################################################################"
    df -h
    echo "#####################################################################"
fi

echo "Done :)"

